<?php

namespace App\Entity;

use App\Repository\ProductRepository;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=ProductRepository::class)
 */
class Product
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private ?int $id = null;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private ?string $name = null; // Change 'Name' to 'name' for consistent naming.

    /**
     * @ORM\Column(type="text") // If your descriptions can be longer than 255 characters.
     */
    private ?string $description = null;

    /**
     * @ORM\Column(type="decimal", precision=10, scale=2) // Use 'decimal' for prices.
     */
    private ?string $price = null; // Change 'Price' to 'price' for consistent naming.

    /**
     * @ORM\Column(type="string", length=255)
     */
    private ?string $category = null; // Change 'Category' to 'category' for consistent naming.

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getName(): ?string
    {
        return $this->name;
    }

    public function setName(string $name): static
    {
        $this->name = $name;

        return $this;
    }

    public function getDescription(): ?string
    {
        return $this->description;
    }

    public function setDescription(string $description): static
    {
        $this->description = $description;

        return $this;
    }

    public function getPrice(): ?string
    {
        return $this->price;
    }

    public function setPrice(string $price): static
    {
        $this->price = $price;

        return $this;
    }

    public function getCategory(): ?string
    {
        return $this->category;
    }

    public function setCategory(string $category): static
    {
        $this->category = $category;

        return $this;
    }
}
